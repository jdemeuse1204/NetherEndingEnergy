package com.agrejus.netherendingenergy.common.models;

public class TopLeftPos {
    private final int top;
    private final int left;

    public TopLeftPos(int top, int left) {
        this.top = top;
        this.left = left;
    }

    public int getTop() {
        return top;
    }

    public int getLeft() {
        return left;
    }
}
